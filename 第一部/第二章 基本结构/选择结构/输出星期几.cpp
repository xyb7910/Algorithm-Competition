//输出星期几 
#include <iostream>
using namespace std;

int main()
{
  int n;
  cin>>n;
  if(n=1) 
    cout<<"星期一"<<endl;
  if(n=2) 
    cout<<"星期二"<<endl;
  if(n=3) 
    cout<<"星期三"<<endl;
  if(n=4) 
    cout<<"星期四"<<endl;
  if(n=5) 
    cout<<"星期五"<<endl;
  if(n=6) 
    cout<<"星期六"<<endl;
  if(n=7) 
    cout<<"星期日"<<endl;
  system("pause");
  return 0;              
}
